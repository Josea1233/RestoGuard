create or replace function public.listar_negocios_admin()
returns table (
  id bigint,
  nombre text,
  rubro text,
  ciudad text,
  estado text,
  plan text,
  owner_id uuid,
  owner_email text,
  beta_started_at timestamp with time zone,
  trial_ends_at timestamp with time zone,
  created_at timestamp with time zone,
  usuarios bigint
)
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.es_super_admin() then
    raise exception 'Solo el Super Admin puede listar negocios';
  end if;

  return query
    select
      n.id,
      n.nombre,
      n.rubro,
      n.ciudad,
      n.estado,
      n.plan,
      n.owner_id,
      u.email::text as owner_email,
      n.beta_started_at,
      n.trial_ends_at,
      n.created_at,
      count(nu.id)::bigint as usuarios
    from public.negocios n
    left join auth.users u on u.id = n.owner_id
    left join public.negocio_usuarios nu on nu.negocio_id = n.id
    group by n.id, u.email
    order by n.created_at desc;
end;
$$;

create or replace function public.crear_negocio_admin(
  p_nombre text,
  p_rubro text,
  p_ciudad text,
  p_owner_email text
)
returns table (
  id bigint,
  nombre text,
  owner_email text,
  trial_ends_at timestamp with time zone
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_owner_id uuid;
  v_owner_email text;
  v_negocio public.negocios;
begin
  if not public.es_super_admin() then
    raise exception 'Solo el Super Admin puede crear negocios';
  end if;

  if nullif(trim(p_nombre), '') is null then
    raise exception 'El nombre del negocio es obligatorio';
  end if;

  select au.id, au.email::text
    into v_owner_id, v_owner_email
  from auth.users au
  where lower(au.email) = lower(trim(p_owner_email))
  limit 1;

  if v_owner_id is null then
    raise exception 'Usuario no encontrado. Primero crealo en Supabase Auth.';
  end if;

  insert into public.negocios (
    owner_id,
    nombre,
    rubro,
    ciudad,
    estado,
    plan,
    beta_started_at,
    trial_ends_at
  )
  values (
    v_owner_id,
    trim(p_nombre),
    nullif(trim(p_rubro), ''),
    nullif(trim(p_ciudad), ''),
    'BETA',
    'BETA',
    now(),
    now() + interval '30 days'
  )
  returning * into v_negocio;

  insert into public.negocio_usuarios (
    negocio_id,
    user_id,
    email,
    rol
  )
  values (
    v_negocio.id,
    v_owner_id,
    lower(v_owner_email),
    'ADMIN'
  )
  on conflict (negocio_id, user_id)
  do update set
    email = excluded.email,
    rol = 'ADMIN';

  return query
    select
      v_negocio.id,
      v_negocio.nombre,
      lower(v_owner_email)::text,
      v_negocio.trial_ends_at;
end;
$$;

create or replace function public.actualizar_estado_negocio(
  p_negocio_id bigint,
  p_estado text
)
returns table (
  id bigint,
  estado text,
  plan text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_estado text := upper(trim(p_estado));
  v_negocio public.negocios;
begin
  if not public.es_super_admin() then
    raise exception 'Solo el Super Admin puede cambiar el estado comercial';
  end if;

  if v_estado not in ('BETA', 'ACTIVO', 'SUSPENDIDO') then
    raise exception 'Estado comercial invalido';
  end if;

  update public.negocios n
    set estado = v_estado,
        plan = case
          when v_estado = 'BETA' then 'BETA'
          when v_estado = 'ACTIVO' and n.plan = 'BETA' then 'BASICO'
          else n.plan
        end
  where n.id = p_negocio_id
  returning * into v_negocio;

  if v_negocio.id is null then
    raise exception 'Negocio no encontrado';
  end if;

  return query select v_negocio.id, v_negocio.estado, v_negocio.plan;
end;
$$;

create or replace function public.agregar_usuario_negocio(
  p_negocio_id bigint,
  p_email text,
  p_rol text default 'MOZO'
)
returns table (
  id bigint,
  email text,
  rol text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid;
  v_user_email text;
  v_member public.negocio_usuarios;
begin
  if not public.es_admin_negocio(p_negocio_id) then
    raise exception 'Solo un ADMIN puede agregar usuarios';
  end if;

  if p_rol not in ('ADMIN', 'SUPERVISOR', 'CAJA', 'MOZO', 'COCINA', 'BARRA', 'RRHH') then
    raise exception 'Rol invalido';
  end if;

  select au.id, au.email::text
    into v_user_id, v_user_email
  from auth.users au
  where lower(au.email) = lower(trim(p_email))
  limit 1;

  if v_user_id is null then
    raise exception 'Usuario no encontrado. Primero crealo en Supabase Auth.';
  end if;

  insert into public.negocio_usuarios (
    negocio_id,
    user_id,
    email,
    rol
  )
  values (
    p_negocio_id,
    v_user_id,
    lower(v_user_email),
    p_rol
  )
  on conflict (negocio_id, user_id)
  do update set
    email = excluded.email,
    rol = excluded.rol
  returning * into v_member;

  return query
    select
      v_member.id,
      v_member.email::text,
      v_member.rol::text;
end;
$$;

