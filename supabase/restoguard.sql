create table if not exists public.negocios (
  id bigint generated by default as identity primary key,
  owner_id uuid references auth.users(id) on delete cascade,
  nombre text not null,
  rubro text,
  ciudad text,
  estado text not null default 'BETA' check (estado in ('BETA', 'ACTIVO', 'SUSPENDIDO')),
  plan text not null default 'BETA',
  beta_started_at timestamp with time zone not null default now(),
  trial_ends_at timestamp with time zone not null default (now() + interval '30 days'),
  created_at timestamp with time zone not null default now()
);

create table if not exists public.negocio_usuarios (
  id bigint generated by default as identity primary key,
  negocio_id bigint not null references public.negocios(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  email text,
  rol text not null default 'ADMIN' check (
    rol in ('ADMIN', 'SUPERVISOR', 'CAJA', 'MOZO', 'COCINA', 'BARRA', 'RRHH')
  ),
  created_at timestamp with time zone not null default now(),
  unique (negocio_id, user_id)
);

create table if not exists public.insumos (
  id bigint generated by default as identity primary key,
  negocio_id bigint not null references public.negocios(id) on delete cascade,
