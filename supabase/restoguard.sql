create or replace function public.es_super_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.app_admins aa
    where aa.user_id = auth.uid()
  );
$$;

create or replace function public.es_miembro_negocio(p_negocio_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.negocio_usuarios nu
      where nu.negocio_id = p_negocio_id
        and nu.user_id = auth.uid()
    );
$$;

create or replace function public.negocio_habilitado(p_negocio_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.negocios n
      where n.id = p_negocio_id
        and n.estado in ('BETA', 'ACTIVO')
    );
$$;

create or replace function public.tiene_acceso_negocio(p_negocio_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_miembro_negocio(p_negocio_id)
    and public.negocio_habilitado(p_negocio_id);
$$;

create or replace function public.es_admin_negocio(p_negocio_id bigint)
returns boolean
language sql
security definer
set search_path = public
as $$
  select public.es_super_admin()
    or exists (
      select 1
      from public.negocio_usuarios nu
      join public.negocios n on n.id = nu.negocio_id
      where nu.negocio_id = p_negocio_id
        and nu.user_id = auth.uid()
        and nu.rol = 'ADMIN'
        and n.estado in ('BETA', 'ACTIVO')
    );
$$;

